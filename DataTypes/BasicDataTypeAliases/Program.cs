namespace BasicTypesaAliases
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Declare and assign values to basic data types
            System.Int32 myInt = 10;                        //int 
            System.Single myFloat = 3.14f;                  //float 
            System.Double myDouble = 2.71828;               //double 
            System.Decimal myDecimail = 2.71828m;           //decimal
            System.Char myChar = 'A';                       //char 
            System.Boolean myBool = true;                   //bool my
            System.String myString = "MyStr";               //string

            // Output the values
            Console.WriteLine("Integer: " + myInt);
            Console.WriteLine("Float: " + myFloat);
            Console.WriteLine("Double: " + myDouble);
            Console.WriteLine("Decimal: " + myDecimail);
            Console.WriteLine("Character: " + myChar);
            Console.WriteLine("Boolean: " + myBool);
            Console.WriteLine("String: " + myString);
        }
    }
}


