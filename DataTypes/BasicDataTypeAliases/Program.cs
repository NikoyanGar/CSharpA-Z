namespace BasicDataTypeAliases
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Declare and assign values to basic data types
            int myInt = 10;                        //int 
            float myFloat = 3.14f;                  //float 
            double myDouble = 2.71828;               //double 
            decimal myDecimail = 2.71828m;           //decimal
            char myChar = 'A';                       //char 
            bool myBool = true;                   //bool my
            string myString = "MyStr";               //string

            // Output the values
            Console.WriteLine("Integer: " + myInt);
            Console.WriteLine("Float: " + myFloat);
            Console.WriteLine("Double: " + myDouble);
            Console.WriteLine("Decimal: " + myDecimail);
            Console.WriteLine("Character: " + myChar);
            Console.WriteLine("Boolean: " + myBool);
            Console.WriteLine("String: " + myString);
        }
    }
}


